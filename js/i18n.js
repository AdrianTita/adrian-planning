function normalizeLang(lang) {
  if (!lang) return DEFAULT_LANG;
  const lower = String(lang).toLowerCase();
  return SUPPORTED_LANGS.includes(lower) ? lower : DEFAULT_LANG;
}

export function t(lang, key) {
  const l = normalizeLang(lang);
  return I18N[l]?.[key] ?? I18N[DEFAULT_LANG]?.[key] ?? null;
}

export function applyTranslations(lang) {
  const l = normalizeLang(lang);
  document.documentElement.lang = l;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const txt = t(l, key);
    if (txt !== null) el.textContent = txt;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    const txt = t(l, key);
    if (txt !== null) el.setAttribute("placeholder", txt);
  });

  document.querySelectorAll("[data-i18n-value]").forEach((el) => {
    const key = el.getAttribute("data-i18n-value");
    const txt = t(l, key);
    if (txt !== null) el.value = txt;
  });
}

export async function initUserLanguage(uid, userData = null, userDocExists = null) {
  let lang = normalizeLang(localStorage.getItem("appLang"));

  if (userData && userData.language) {
    lang = normalizeLang(userData.language);
  }

  if (!lang) {
    lang = DEFAULT_LANG;
  }

  try {
    if (userDocExists === null) {
      const snap = await getDoc(doc(db, "users", uid));
      if (snap.exists()) {
        const data = snap.data() || {};
        if (data.language) {
          lang = normalizeLang(data.language);
        } else {
          await setDoc(doc(db, "users", uid), { language: lang }, { merge: true });
        }
      } else {
        await setDoc(doc(db, "users", uid), { language: lang }, { merge: true });
      }
    } else if (!userDocExists || !userData || !userData.language) {
      await setDoc(doc(db, "users", uid), { language: lang }, { merge: true });
    }
  } catch (e) {
    console.warn("Falha a ler/gravar idioma do utilizador:", e);
  }

  localStorage.setItem("appLang", lang);
  applyTranslations(lang);
  return lang;
}

export async function setUserLanguage(uid, lang) {
  const l = normalizeLang(lang);
  localStorage.setItem("appLang", l);
  applyTranslations(l);
  try {
    await setDoc(doc(db, "users", uid), { language: l }, { merge: true });
  } catch (e) {
    console.warn("Falha a guardar idioma do utilizador:", e);
  }
  return l;
}

export function bindLanguageSelector(selectEl, uid, currentLang) {
  if (!selectEl) return;
  const initial = normalizeLang(currentLang || localStorage.getItem("appLang"));
  if (initial) selectEl.value = initial;

  selectEl.addEventListener("change", async (e) => {
    const next = normalizeLang(e.target.value);
    await setUserLanguage(uid, next);
  });
}
